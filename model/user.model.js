const mongoose = require("mongoose");

// create shcema
const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

// create mdoel
module.exports = mongoose.model("Users", userSchema);
